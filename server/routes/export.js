// HELPER: Build NoSQL — actual JSON documents array
// ══════════════════════════════════════════════════════════════════════════════
function buildNoSQL(tableName, attributes, rawContent) {
  const { headers, rows } = parseCSV(rawContent);

  // Type coerce values based on dm_attributes data_type
  const typeMap = {};
  for (const a of attributes) {
    const t = (a.data_type || '').toUpperCase();
    if (t.startsWith('INT') || t === 'BIGINT' || t === 'SMALLINT' || t === 'TINYINT') {
      typeMap[a.column_name] = 'int';
    } else if (t.startsWith('DECIMAL') || t.startsWith('FLOAT') || t.startsWith('DOUBLE')) {
      typeMap[a.column_name] = 'float';
    } else if (t === 'BOOLEAN' || t === 'BOOL' || t === 'TINYINT(1)') {
      typeMap[a.column_name] = 'bool';
    } else {
      typeMap[a.column_name] = 'string';
    }
  }

  const coerce = (val, type) => {
    if (val === null || val === undefined || val === '') return null;
    if (type === 'int')   return parseInt(val,   10) || 0;
    if (type === 'float') return parseFloat(val)     || 0.0;
    if (type === 'bool')  return val === 'true' || val === '1' || val === 'yes';
    return val;
  };

  const docs = rows.map(row => {
    const doc = {};
    for (const h of headers) {
      if (typeMap[h] !== undefined) {
        doc[h] = coerce(row[h], typeMap[h]);
      } else {
        doc[h] = row[h];
      }
    }
    return doc;
  });

  // Build the Mongoose schema definition too (for reference)
  const schemaFields = {};
  for (const a of attributes) {
    const t = typeMap[a.column_name] || 'string';
    const mongoType = t === 'int' || t === 'float' ? 'Number'
      : t === 'bool' ? 'Boolean' : 'String';
    const def = { type: mongoType, required: a.is_nullable === 'NO' };
    if (a.is_primary === 'YES' || a.constraints?.toUpperCase().includes('UNIQUE')) def.unique = true;
    if (a.default_value) def.default = a.default_value;
    schemaFields[a.column_name] = def;
  }

  const schemaJSON = JSON.stringify(schemaFields, null, 2)
    .replace(/"Number"/g, 'Number')
    .replace(/"Boolean"/g, 'Boolean')
    .replace(/"String"/g, 'String')
    .replace(/"Date"/g, 'Date');

  const mongooseCode =
    `const mongoose = require('mongoose');\n\n` +
    `const ${tableName}Schema = new mongoose.Schema(\n${schemaJSON},\n` +
    `  { timestamps: true }\n);\n\n` +
    `module.exports = mongoose.model('${tableName}', ${tableName}Schema);\n`;

  const indexes = attributes
    .filter(a => a.is_primary === 'YES' || a.constraints?.toUpperCase().includes('UNIQUE'))
    .map(a => `db.${tableName}.createIndex({ ${a.column_name}: 1 }, { unique: true });`);

  return {
    collectionName: tableName,
    documents:      docs,
    documentCount:  docs.length,
    mongooseCode,
    indexes,
  };
}

// ══════════════════════════════════════════════════════════════════════════════
// HELPER: Fetch table + attributes + raw CSV
// ══════════════════════════════════════════════════════════════════════════════
async function getTableAndAttrs(tableId, uid) {
  const [tables] = await pool.execute(
    `SELECT tm.*, di.raw_content
       FROM dm_table_master tm
       LEFT JOIN dm_input_data di ON di.data_id = tm.data_id
      WHERE tm.table_id = ? AND tm.user_uid = ?`,
    [tableId, uid]
  );
  if (!tables.length) throw { code: 404, message: 'Table not found.' };

  const [attrs] = await pool.execute(
    'SELECT * FROM dm_attributes WHERE table_id = ? ORDER BY attr_id ASC',
    [tableId]
  );
  if (!attrs.length) throw { code: 400, message: 'No columns found. Please complete the schema builder first.' };

  return {
    table:      tables[0],
    attrs,
    rawContent: tables[0].raw_content || '',
  };
}

// ══════════════════════════════════════════════════════════════════════════════
// GET /api/export/sql/:tableId?uid=
// ══════════════════════════════════════════════════════════════════════════════
router.get('/sql/:tableId', async (req, res) => {
  const { tableId } = req.params;
  const { uid }     = req.query;
  if (!uid) return res.status(400).json({ error: 'uid is required.' });

  try {
    const { table, attrs, rawContent } = await getTableAndAttrs(tableId, uid);
    const sql = buildSQL(table.table_name, attrs, rawContent);
    const { rows } = parseCSV(rawContent);

    pool.execute(
      'INSERT INTO dm_exports (table_id, user_uid, export_type) VALUES (?, ?, "SQL")',
      [tableId, uid]
    ).catch(() => {});

    res.json({ tableName: table.table_name, exportType: 'SQL', sql, rowCount: rows.length, columnCount: attrs.length });
  } catch (err) {
    if (err.code) return res.status(err.code).json({ error: err.message });
    console.error('[Export/SQL Error]', err);
    res.status(500).json({ error: 'Failed to generate SQL.', details: err.message });
  }
});

// ══════════════════════════════════════════════════════════════════════════════
// GET /api/export/sql/:tableId/download?uid=
// ══════════════════════════════════════════════════════════════════════════════
router.get('/sql/:tableId/download', async (req, res) => {
  const { tableId } = req.params;
  const { uid }     = req.query;
  if (!uid) return res.status(400).json({ error: 'uid is required.' });

  try {
    const { table, attrs, rawContent } = await getTableAndAttrs(tableId, uid);
    const sql = buildSQL(table.table_name, attrs, rawContent);

    pool.execute(
      'INSERT INTO dm_exports (table_id, user_uid, export_type) VALUES (?, ?, "SQL")',
      [tableId, uid]
    ).catch(() => {});

    res.setHeader('Content-Type',        'text/plain; charset=utf-8');
    res.setHeader('Content-Disposition', `attachment; filename="${table.table_name}.sql"`);
    res.send(sql);
  } catch (err) {
    if (err.code) return res.status(err.code).json({ error: err.message });
    console.error('[Export/SQL/download Error]', err);
    res.status(500).json({ error: 'Failed to download SQL.' });
  }
});

