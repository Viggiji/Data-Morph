// ══════════════════════════════════════════════════════════════════════════════
// GET /api/export/nosql/:tableId/download?uid=   → downloads .json (real docs)
// ══════════════════════════════════════════════════════════════════════════════
router.get('/nosql/:tableId/download', async (req, res) => {
  const { tableId } = req.params;
  const { uid }     = req.query;
  if (!uid) return res.status(400).json({ error: 'uid is required.' });

  try {
    const { table, attrs, rawContent } = await getTableAndAttrs(tableId, uid);
    const nosql = buildNoSQL(table.table_name, attrs, rawContent);

    const header =
      `// Generated by DATA MORPH\n` +
      `// Collection: ${table.table_name}\n` +
      `// Documents:  ${nosql.documentCount}\n` +
      `// Exported:   ${new Date().toISOString()}\n\n`;

    // Export as a proper JSON array the user can import into mongo:
    // mongoimport --jsonArray --file employees.json --collection employees
    const jsonContent =
      header +
      `// === MONGOOSE MODEL ===\n` +
      nosql.mongooseCode + '\n' +
      (nosql.indexes.length ? `// Indexes:\n${nosql.indexes.join('\n')}\n\n` : '') +
      `// === DATA (${nosql.documentCount} documents) ===\n` +
      `// Copy the JSON below and use: mongoimport --jsonArray\n` +
      JSON.stringify(nosql.documents, null, 2) + '\n';

    pool.execute(
      'INSERT INTO dm_exports (table_id, user_uid, export_type) VALUES (?, ?, "NoSQL")',
      [tableId, uid]
    ).catch(() => {});

    res.setHeader('Content-Type',        'text/plain; charset=utf-8');
    res.setHeader('Content-Disposition', `attachment; filename="${table.table_name}.nosql.js"`);
    res.send(jsonContent);
  } catch (err) {
    if (err.code) return res.status(err.code).json({ error: err.message });
    console.error('[Export/NoSQL/download Error]', err);
    res.status(500).json({ error: 'Failed to download NoSQL export.' });
  }
});

export default router;
