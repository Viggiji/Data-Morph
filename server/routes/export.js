/**
 * export.js — SQL & NoSQL Export Routes (WITH DATA)
 *
 * Now exports the full table: schema DDL + actual data rows.
 *
 *   GET /api/export/sql/:tableId?uid=              — SQL as JSON
 *   GET /api/export/sql/:tableId/download?uid=     — Download .sql
 *   GET /api/export/nosql/:tableId?uid=            — NoSQL JSON
 *   GET /api/export/nosql/:tableId/download?uid=   — Download .json
 */

import { Router } from 'express';
import pool from '../db.js';

const router = Router();

// ══════════════════════════════════════════════════════════════════════════════
// HELPER: Parse raw CSV string → { headers, rows }
// ══════════════════════════════════════════════════════════════════════════════
function parseCSV(rawContent) {
  if (!rawContent) return { headers: [], rows: [] };

  const lines = rawContent.trim().split('\n').filter(l => l.trim());
  if (lines.length < 1) return { headers: [], rows: [] };

  // Parse a single CSV line respecting double-quoted fields
  const parseLine = (line) => {
    const result = [];
    let cur = '';
    let inQuote = false;
    for (let i = 0; i < line.length; i++) {
      const ch = line[i];
      if (ch === '"') {
        inQuote = !inQuote;
      } else if (ch === ',' && !inQuote) {
        result.push(cur.trim());
        cur = '';
      } else {
        cur += ch;
      }
    }
    result.push(cur.trim());
    return result;
  };

  const headers = parseLine(lines[0]);
  const rows = [];
  for (let i = 1; i < lines.length; i++) {
    if (!lines[i].trim()) continue;
    const values = parseLine(lines[i]);
    const row = {};
    headers.forEach((h, idx) => {
      row[h] = values[idx] !== undefined ? values[idx] : null;
    });
    rows.push(row);
  }

  return { headers, rows };
}

// ══════════════════════════════════════════════════════════════════════════════
// HELPER: Escape a value for SQL INSERT
// ══════════════════════════════════════════════════════════════════════════════
function sqlEscape(val) {
  if (val === null || val === undefined || val === '') return 'NULL';
  const str = String(val);
  // If it looks like a pure integer or decimal, don't quote it
  if (/^-?\d+$/.test(str))          return str;
  if (/^-?\d+\.\d+$/.test(str))     return str;
  // Escape backslashes and single quotes
  return `'${str.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
}

// ══════════════════════════════════════════════════════════════════════════════
// HELPER: Build SQL CREATE TABLE + INSERT INTO
// ══════════════════════════════════════════════════════════════════════════════
function buildSQL(tableName, attributes, rawContent) {
  // ── DDL ────────────────────────────────────────────────────────────────────
  const colLines = attributes.map(attr => {
    let line = `  \`${attr.column_name}\` ${attr.data_type}`;
    if (attr.is_nullable === 'NO') line += ' NOT NULL';
    if (attr.default_value)        line += ` DEFAULT ${attr.default_value}`;
    return line;
  });

  const tableConstraints = [];

  // Primary key
  const pkCol = attributes.find(a => a.is_primary === 'YES');
  if (pkCol) tableConstraints.push(`  PRIMARY KEY (\`${pkCol.column_name}\`)`);

  // Unique keys
  for (const a of attributes) {
    if (a.constraints?.toUpperCase().includes('UNIQUE')) {
      tableConstraints.push(`  UNIQUE KEY \`uq_${a.column_name}\` (\`${a.column_name}\`)`);
    }
  }

  // Check constraints
  for (const a of attributes) {
    if (a.constraints) {
      const m = a.constraints.match(/CHECK\s*\(([^)]*)\)/i);
      if (m) tableConstraints.push(`  CONSTRAINT \`chk_${tableName}_${a.column_name}\` ${m[0]}`);
    }
  }

  // Foreign keys
  for (const a of attributes) {
    if (a.is_foreign === 'YES' && a.foreign_ref) {
      const parts = a.foreign_ref.split('.');
      if (parts.length === 2) {
        const [refTable, refCol] = parts;
        tableConstraints.push(
          `  CONSTRAINT \`fk_${tableName}_${a.column_name}\`\n` +
          `    FOREIGN KEY (\`${a.column_name}\`)\n` +
          `    REFERENCES \`${refTable}\`(\`${refCol}\`)\n` +
          `    ON DELETE CASCADE ON UPDATE CASCADE`
        );
      }
    }
  }

  const ddl =
    `-- Generated by DATA MORPH\n` +
    `-- Table: ${tableName}\n` +
    `-- Exported: ${new Date().toISOString()}\n\n` +
    `CREATE TABLE IF NOT EXISTS \`${tableName}\` (\n` +
    [...colLines, ...tableConstraints].join(',\n') +
    `\n) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;\n`;

  // ── INSERT INTO (with actual data) ────────────────────────────────────────
  const { headers, rows } = parseCSV(rawContent);

  if (rows.length === 0) {
    return ddl + '\n-- No data rows found.\n';
  }

  // Only use the columns that actually exist in dm_attributes
  const attrNames = attributes.map(a => a.column_name);
  const usedHeaders = headers.filter(h => attrNames.includes(h));

  if (usedHeaders.length === 0) {
    return ddl + '\n-- Column mismatch: no matching columns found in CSV.\n';
  }

  const colList = usedHeaders.map(h => `\`${h}\``).join(', ');

  // Chunk inserts in groups of 500 for readability
  const CHUNK = 500;
  const insertBlocks = [];
  for (let i = 0; i < rows.length; i += CHUNK) {
    const chunk = rows.slice(i, i + CHUNK);
    const valueRows = chunk.map(row => {
      const vals = usedHeaders.map(h => sqlEscape(row[h]));
      return `  (${vals.join(', ')})`;
    });
    insertBlocks.push(
      `INSERT INTO \`${tableName}\` (${colList}) VALUES\n${valueRows.join(',\n')};\n`
    );
  }

  return ddl + '\n' + insertBlocks.join('\n');
}

